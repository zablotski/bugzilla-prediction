function getAssignedTimeFromBugHistory(bugHistory){

    var t = bugHistory.history[0].history.filter(function (el) {
        if(el.changes.filter(function(ch){
           return  ch.added == "ASSIGNED";
      }).length){
            return true;
        }
    });

    return t[0].when;

}


